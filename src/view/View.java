    private void tblItemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblItemMouseClicked
        addNewItem = false;
        int pos = tblItem.getSelectedRow();
        if(pos == -1)
            return;
        txtItemCode.setText(tblItem.getValueAt(pos, 0).toString());
        txtItemName.setText(tblItem.getValueAt(pos, 1).toString());
        int selectedItem = 0;
        setComboBox();
        try 
        {
            for(Supplier  s : SupplierDAO.getAllSuppliers())
            {
                if(tblItem.getValueAt(pos, 2).toString().contains(s.getSupplierCode()))
                {
                    for(int i=0; i< cbSupplier.getItemCount(); i++)
                    {
                        if(cbSupplier.getItemAt(i).toString().contains(s.getSupplierCode()))
                            selectedItem = i;
                    }
                }
            }
        } 
        catch (Exception ex) 
        {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        }
        cbSupplier.setSelectedIndex(selectedItem);
        txtItemUnit.setText(tblItem.getValueAt(pos, 3).toString());
        txtItemPrice.setText(tblItem.getValueAt(pos, 4).toString());
        ckbSupplying.setSelected(tblItem.getValueAt(pos, 5).toString().equalsIgnoreCase("true"));
        btnItemDelete.setEnabled(true);
        txtItemCode.setEnabled(false);
        /////
        unlockItemInput();
    }//GEN-LAST:event_tblItemMouseClicked

