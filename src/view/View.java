    private void btnSuppSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuppSaveActionPerformed
        String code = txtSuppCode.getText().toUpperCase().trim();
        String name = txtSuppName.getText().trim();
        String address = txtSuppAddress.getText().trim();
        boolean collaborating = ckbCollaborating.isSelected();
        if(!SupplierDAO.validateSupplierData(addNewSupp, code, name, address))
            return;
        Supplier s = new Supplier(code, name, address, collaborating);
        if(addNewSupp==true)
        {
            try 
            {
                if(SupplierDAO.getSupplierByCode(code)!=null)
                {
                    JOptionPane.showMessageDialog(this, "Duplicate code!");
                    return;
                }
                int result = SupplierDAO.insertSupplier(s);
                if(result==SUCCESS)
                {
                    supplierModel.getData().add(s);
                    this.tblSupplier.setModel(supplierModel);
                    JOptionPane.showMessageDialog(this, "Add successfully!");
                }
                else
                    JOptionPane.showMessageDialog(this, "Add failed!");
            }
            catch (Exception ex) 
            {
                Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
            int pos = tblSupplier.getSelectedRow();
            try 
            {              
                int result = SupplierDAO.updateSupplier(s);
                if(result==SUCCESS)
                {
                    supplierModel.getData().set(pos, s);
                    this.tblSupplier.setModel(supplierModel);
                    JOptionPane.showMessageDialog(this, "Update successfully!");
                }
                else
                    JOptionPane.showMessageDialog(this, "Update failed!");
            } 
            catch (Exception ex) 
            {
                Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        tblSupplier.updateUI();
        clearSupplierInfo();
        setComboBox();
        addNewSupp = false;
        /////
        lockSupplierInput();
    }//GEN-LAST:event_btnSuppSaveActionPerformed

    private void btnItemSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnItemSaveActionPerformed
        String code = txtItemCode.getText().toUpperCase().trim();
        String name = txtItemName.getText().trim();
        String unit = txtItemUnit.getText().trim();
        String price = txtItemPrice.getText().trim();
        int item = cbSupplier.getSelectedIndex();
        String supplier = cbSupplier.getItemAt(item);
        String supplierCode = null;
        try 
        {
            for(Supplier s : SupplierDAO.getAllSuppliers())
            {
                if(supplier.contains(s.getSupplierCode()))
                    supplierCode = s.getSupplierCode();
            }
        } 
        catch (Exception ex) 
        {
            Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
        }
        boolean supplying = ckbSupplying.isSelected();
        if(!ItemDAO.validateItemData(addNewItem, code, name, unit, price))
            return;
        Item newItem = new Item(code, name, unit, Float.parseFloat(price), supplierCode, supplying);
//        Item itemDTB = new Item(code, name, unit, Float.parseFloat(price), supplierCode, supplying);
        if(addNewItem==true)
        {
            try 
            {
                if(ItemDAO.getItemByCode(code)!=null)
                {
                    JOptionPane.showMessageDialog(this, "Duplicate code!");
                    return;
                }              
                int result = ItemDAO.insertItem(newItem);
                if(result==SUCCESS)
                {
                    itemModel.getData().add(newItem);
                    this.tblItem.setModel(itemModel);
                    JOptionPane.showMessageDialog(this, "Add successfully!");
                }
                else
                    JOptionPane.showMessageDialog(this, "Add failed!");
            } 
            catch (Exception ex) 
            {
                Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else
        {
            int pos = tblItem.getSelectedRow();
            try 
            {
                int result = ItemDAO.updateItem(newItem);
                if(result==SUCCESS)
                {
                    itemModel.getData().set(pos, newItem);
                    this.tblItem.setModel(itemModel);
                    JOptionPane.showMessageDialog(this, "Update successfully!");
                }
                else
                    JOptionPane.showMessageDialog(this, "Update failed!");
            } 
            catch (Exception ex) 
            {
                Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
            } 
        }
        tblItem.updateUI();
        clearItemInfo();
        addNewItem = false;
        /////
        lockItemInput();
    }//GEN-LAST:event_btnItemSaveActionPerformed

    private void btnSuppDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuppDeleteActionPerformed
        int confirm;
        confirm = JOptionPane.showConfirmDialog(this, "Do you want to remove this supplier?");
        if(confirm == JOptionPane.YES_OPTION)
        {
            int selectedRow=tblSupplier.getSelectedRow();
            String code = tblSupplier.getValueAt(selectedRow, 0).toString();
            boolean check = false;
            try 
            {
                for (Item i : ItemDAO.getAllItems()) 
                {
                    if(i.getSupplier().equalsIgnoreCase(tblSupplier.getValueAt(selectedRow, 0).toString()))
                    {
                        check = true;
                    }
                }
                if(check == true)
                {
                    JOptionPane.showMessageDialog(this, "You can not remove this supplier!!!");
                }
                else
                {
                    int delete = SupplierDAO.deleteSupplier(code);
                    if(delete==SUCCESS)
                    {
                        supplierModel.getData().remove(selectedRow);
                    }
                    JOptionPane.showMessageDialog(this, "This supplier is removed!");
                }
            }   
            catch (Exception ex) 
            {
                Logger.getLogger(View.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        tblSupplier.updateUI();
        clearSupplierInfo();
        txtSuppCode.setEnabled(true);
        /////
        lockSupplierInput();
    }//GEN-LAST:event_btnSuppDeleteActionPerformed

